package Gun14;

public class _02_forLoop {
    public static void main(String[] args) {
        // 1 den 5 e kadar olan sayıları ekrana yazdırınız.

        // döngünü kaç kere döneceği belli olmayan durumlarda kullanacağız

        for(int i=1;i<6;i++){
            System.out.println(i);
        }


    }
}
